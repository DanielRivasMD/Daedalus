////////////////////////////////////////////////////////////////////////////////////////////////////

use anyhow::Result as anyResult;
use clap::Parser;

////////////////////////////////////////////////////////////////////////////////////////////////////

mod cli;
mod cmd;
mod core;
mod util;

////////////////////////////////////////////////////////////////////////////////////////////////////

fn main() -> anyResult<()> {
    let cli = cli::Cli::parse();

    match cli.command {
        None => core::run(cli)?,
        Some(cli::Command::Identity) => cmd::identity::run()?,
        Some(cli::Command::Completion { shell }) => cmd::completion::run(shell)?,
    }
    Ok(())
}

